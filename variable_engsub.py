# ==================
# Variables
# ==================
# A variable is a feature in programming languages.
# Function: To store a value/data.

# A variable can be thought of as a box:
# - The box has a name.
# - The box can store a value.
# - Typically, the box contains only one value/data.

# name = 'Harish'
# print('My name is', name)

# name = 'Bobby'
# print('My name is', name)

# Trivia:
# To comment: Ctrl + /
# To select the next occurrence: Ctrl + D

# age = 29  # Variables can store numbers.
# print('Age is', age)

# =====================
# Variable Naming Rules
# =====================

# Correct naming conventions:
# camelCase
# studentNamePurwadhika = 'Banu'
# print(studentNamePurwadhika)

# snake_case
# student_name_pwdk = 'Reza'
# print(student_name_pwdk)

# Example starting with an underscore:
# _nama_siswa_pwdk = 'Tono'
# print(_nama_siswa_pwdk)

# =============================
# Incorrect Variable Naming
# =============================

# Cannot start with a number
# 2student_bandung = 'Rexi'
# print(2student_bandung)

# Cannot contain punctuation
# student_pwdk? = 'Rexa'
# print(student_pwdk?)

# Cannot contain spaces
# student pwdk = 'Joko'
# print(student pwdk)

